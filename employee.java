package conn;

public class employee {
        private int id;
        private String name;
        private String email;
        private float gpa;
        private float rate;

        public employee() {
            super();
        }

        public employee(int id, String name, String email, float gpa, float rate) {
            super();
            this.id = id;
            this.name = name;
            this.email = email;
            this.gpa = gpa;
            this.rate = rate;
        }

        public int getId() {
            return id;
        }

        public void setId(int id) {
            this.id = id;
        }

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }


        public String getEmail() {
            return email;
        }

        public void setEmail(String email) {
            this.email = email;
        }
        public float getGpa() {
            return gpa;
        }
        public void setGpa(float gpa) {
            this.gpa = gpa;
        }
        public float getRate() {
            return rate;
        }
        public void setRate(float rate) {
            this.rate = rate;
        }

    }
    


    
